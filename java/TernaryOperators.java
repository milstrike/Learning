public class TernaryOperators {
    public static void main(String[] args){
        int x, y;
        x = 10;
        y = ( x == 1 ) ? 20: 30;
        System.out.println("Value of y is: " + y);
        
        y = ( x == 10 ) ? 20: 30;
        System.out.println("Value of y is: " + y);
    }
}


